MSG1 DB 0AH,0DH,"ENTER THE STRING:$"
MSG2 DB 0AH,0DH,"STRING ENTERED IS:$"
MSG3 DB 10D DUP(?)
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:MOV AX,DATA
	MOV DS,AX
	
	MOV AH,09H
	LEA DX,MSG1
	INT 21H
	LEA SI,MSG3
	MOV CL,00H
	
LABEL2:MOV AH,01H
	 INT 21H
	 
	 CMP AL,0DH
	 JZ LABEL1
	 MOV[SI],AL
	 INC SI
	 INC CL
	 JMP LABEL2
	 
LABEL1:MOV AH,09H
	 LEA DX,MSG2
	 INT 21H
	 LEA SI,MSG3
	
LABEL3:MOV AH,02H
	 MOV DL,[SI]
	 INT 21H
	 INC SI
	 DEC CL
	 JNZ LABEL3
	 MOV AH,4CH
	 INT 21H
	 
CODE ENDS
END START
